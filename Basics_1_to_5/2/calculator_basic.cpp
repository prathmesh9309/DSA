#include <iostream>
using namespace std;

int main() {
    int num1;
    int num2;
    cout << "Enter the first number :";
    cin >> num1;
    cout << "Enter the second number :";
    cin >> num2;

    cout << "addition  " << (num1 + num2) << endl;
    cout << "subtraction  " << (num1 - num2) << endl;
    cout << "division  " << (num1 / num2) << endl;
    cout << "multiplication  " << (num1 * num2) << endl;
    return 0;
}